package com.example.Metadata;

public class ResponseObject {
	public Object successResponse;
	public Object dataResponse;
	public Object errorResponse;
	public Object getSuccessResponse() {
		return successResponse;
	}
	public void setSuccessResponse(Object successResponse) {
		this.successResponse = successResponse;
	}
	public Object getDataResponse() {
		return dataResponse;
	}
	public void setDataResponse(Object dataResponse) {
		this.dataResponse = dataResponse;
	}
	public Object getErrorResponse() {
		return errorResponse;
	}
	public void setErrorResponse(Object errorResponse) {
		this.errorResponse = errorResponse;
	}

	
}
